; RAW Prototype
include "hardware.inc"  


section "Header", ROM0[$100]
    jp EntryPoint
    ds $150 - @, 0 ; Make room for the header

EntryPoint:
    ; Do not turn the LCD off outside of VBlank
WaitVBlank:
    ld a, [rLY]
    cp 144
    jp c, WaitVBlank

    ; Turn the LCD off
    ld a, 0
    ld [rLCDC], a


    ; Copy the tile data
    ld de, Tiles
    ld hl, $9000
    ld bc, TilesEnd - Tiles
    call Memcopy

    ; Copy the tilemap
    ld de, Tilemap
    ld hl, $9800
    ld bc, TilemapEnd - Tilemap
    call Memcopy


; Copy the tile data
    ld de, Paddle
    ld hl, $8000
    ld bc, PaddleEnd - Paddle
    call Memcopy

    ;
    ld a, 0
    ld b, 160
    ld hl, _OAMRAM

ClearOam:
    ld [hli], a
    dec b
    jp nz, ClearOam

    ld hl, _OAMRAM
    ld a, 128 + 16
    ld [hli], a
    ld a, 16 + 8
    ld [hli], a
    ld a, 0
    ld [hli], a
    ld [hl], a


    ; Turn the LCD on 
    ld a, LCDCF_ON | LCDCF_BGON | LCDCF_OBJON
    ld [rLCDC], a

    ; During the first (blank) frame, initiallize display registers
    ld a, %11100100
    ld [rBGP], a
    ld a, %11100100
    ld [rOBP0], a
Main:
    ; Wait until it's not VBlank
    ld a, [rLY]
    cp 144
    jp nc, Main
WaitVBlank2:
    ld a, [rLY]
    cp 144
    jp c, WaitVBlank2

    ; Check Keys
    call UpdateKeys
    call StopSound

    ; First, check if left button pressed


CheckPress:
    ld a, [wCurKeys]
    cp 0
    jp z, Main
    call PlaySound

    ; Move the paddle one pixel to the right
    ld a, [_OAMRAM + 1]
    inc a
    ld [_OAMRAM + 1], a
    jp Main


Memcopy:
    ld a, [de]
    ld [hli], a
    inc de
    dec bc
    ld a, b
    or a,c 
    jp nz, Memcopy
    ret

UpdateKeys:
    ; Poll half the controller
    ld a, P1F_GET_BTN
    call .onenibble
    ld b, a ; B7-4 = 1; B3-0 = unpressed buttons
  
    ; Poll the other half
    ld a, P1F_GET_DPAD
    call .onenibble
    swap a ; A3-0 = unpressed directions; A7-4 = 1
    xor a, b ; A = pressed buttons + directions
    ld b, a ; B = pressed buttons + directions
  
    ; And release the controller
    ld a, P1F_GET_NONE
    ldh [rP1], a
  
    ; Combine with previous wCurKeys to make wNewKeys
    ld a, [wCurKeys]
    xor a, b ; A = keys that changed state
    and a, b ; A = keys that changed to pressed
    ld [wNewKeys], a
    ld a, b
    ld [wCurKeys], a
    ret
  
  .onenibble
    ldh [rP1], a ; switch the key matrix
    call .knownret ; burn 10 cycles calling a known ret
    ldh a, [rP1] ; ignore value while waiting for the key matrix to settle
    ldh a, [rP1]
    ldh a, [rP1] ; this read counts
    or a, $F0 ; A7-4 = 1; A3-0 = unpressed keys
  .knownret
    ret

PlaySoundFromRegB:
	; Activate audio system
    ld a, $80 ; Bit 7 a 1 para activar el sistema de sonido
    ld [rNR52], a

    ; Configurar la longitud del tono y el ciclo de trabajo para el Canal 2
    ld a, %01000000 ; NR21 - Longitud del tono (los 6 bits menos significativos), Ciclo de trabajo (los 2 bits más significativos)
    ld [rNR21], a

    ; Configurar la envolvente de volumen para el Canal 2
    ld a, %11110010 ; NR22 - Inicializar volumen (primeros 4 bits), Dirección de la envolvente (bit 3), Periodo de la envolvente (últimos 3 bits)
    ld [rNR22], a

    ; Configurar la frecuencia baja del tono (1000Hz) para el Canal 2
    ;ld a, b ; NR23 - Parte baja de la frecuencia del tono (1000Hz)
    ld a, b 
    ld [rNR23], a

    ; Configurar la frecuencia alta del tono (1000Hz) y activar el canal para el Canal 2
    ld a, %10000111 ; NR24 - Parte alta de la frecuencia del tono (primeros 3 bits), Iniciar y parar el tono (bit 7)
    ld [rNR24], a

    ret

PlaySound:
    ld b, 0
    ; A and B has same sound, modify B to append extra sound
    ld a, [wCurKeys]
    and a, PADF_B
    jp z, PlaySound1
    ld b, $77
PlaySound1:
    ld a, [wCurKeys]
    and a, PADF_A
    jp z, PlaySound2
    ld b, $57

PlaySound2:
	; Activate audio system
    ld a, $80 ; Bit 7 a 1 para activar el sistema de sonido
    ld [rNR52], a

    ; Configurar la longitud del tono y el ciclo de trabajo para el Canal 2
    ld a, %01000000 ; NR21 - Longitud del tono (los 6 bits menos significativos), Ciclo de trabajo (los 2 bits más significativos)
    ld [rNR21], a

    ; Configurar la envolvente de volumen para el Canal 2
    ld a, %11110010 ; NR22 - Inicializar volumen (primeros 4 bits), Dirección de la envolvente (bit 3), Periodo de la envolvente (últimos 3 bits)
    ld [rNR22], a

    ; Configurar la frecuencia baja del tono (1000Hz) para el Canal 2
    ;ld a, b ; NR23 - Parte baja de la frecuencia del tono (1000Hz)
    ld a, [wCurKeys]
    add a, b ; add B pressed value
    ld [rNR23], a

    ; Configurar la frecuencia alta del tono (1000Hz) y activar el canal para el Canal 2
    ld a, %10000111 ; NR24 - Parte alta de la frecuencia del tono (primeros 3 bits), Iniciar y parar el tono (bit 7)
    ld [rNR24], a

    ret


StopSound:
    ; Detener el sonido en el Canal 2
    ld a, %00000000 ; NR24 - Iniciar y parar el tono (bit 7 a 0)
    ld [rNR24], a
    ret

Done:
    jp Done

